package com.solvd.hospital.bin;

public class Administratives {
    int administrativesId;

    public Administratives() {
    }

    public Administratives(int administrativesId) {
        this.administrativesId = administrativesId;
    }

    public int getAdministrativesId() {
        return administrativesId;
    }

    public void setAdministrativesId(int administrativesId) {
        this.administrativesId = administrativesId;
    }

    @Override
    public int hashCode() {
        return hashCode();
    }

    @Override
    public boolean equals(Object obj) {
        return equals(obj);
    }

    @Override
    public String toString() {
        return "Administratives{" +
                "administrativesId=" + administrativesId +
                '}';
    }
}
